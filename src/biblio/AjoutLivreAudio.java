    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // Récupérer les informations du formulaire
        String titre = jTextField3.getText();
        String auteur = jTextField4.getText();
        int anneePublication = Integer.parseInt(jTextField6.getText());
        String ISBN = jTextField2.getText();
        int duree = Integer.parseInt(jTextField7.getText());

        // Créer un nouvel essai avec les informations récupérées
        LivreAudio nouvelAudio = new LivreAudio(titre, auteur, anneePublication, ISBN, duree);

        // Ajouter le nouvel essai à la bibliothèque
        Bibliotheque.ajouterLivre(nouvelAudio);

        // Afficher un message de confirmation
        JOptionPane.showMessageDialog(this, "Livre Audio ajouté avec succès.");

        // Effacer les champs du formulaire après l'ajout
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
    }//GEN-LAST:event_jButton1MouseClicked

