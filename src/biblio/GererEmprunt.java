    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Récupérer les informations saisies par l'utilisateur
    int idLivre = Integer.parseInt(jTextField2.getText());
    int idUtilisateur = Integer.parseInt(jTextField3.getText());
    String dateEmission = jTextField1.getText();
    String dateRetour = jTextField4.getText();

    // Rechercher l'utilisateur dans la bibliothèque
    Utilisateur utilisateur = Bibliotheque.getInstance().getUtilisateurById(idUtilisateur);

    if (utilisateur != null) {
        // Récupérer le livre emprunté
        Livre livreEmprunte = Bibliotheque.getInstance().getLivreByISBN(idLivre);

        if (livreEmprunte != null) {
            // Ajouter le livre emprunté à la liste des livres empruntés par l'utilisateur
            Bibliotheque.getInstance().ajouterEmpruntUtilisateur(utilisateur, livreEmprunte);

            // Afficher un message de confirmation à l'utilisateur
            JOptionPane.showMessageDialog(this, "Emprunt enregistré avec succès.");
        } else {
            JOptionPane.showMessageDialog(this, "Livre non trouvé.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Utilisateur non trouvé.");
    }

    // Effacer les champs du formulaire après l'ajout
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

