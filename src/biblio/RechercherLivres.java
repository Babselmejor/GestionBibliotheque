    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // Récupérer l'ISBN saisi par l'utilisateur
    String isbn = jTextField2.getText();
    
    // Rechercher le livre dans la bibliothèque en fonction de l'ISBN
    Livre livreTrouve = Bibliotheque.rechercherParISBN(isbn);
    
    // Vérifier si le livre a été trouvé
    if (livreTrouve != null) {
        // Vérifier le type de livre et afficher les détails en conséquence
        if (livreTrouve instanceof Roman) {
            Roman roman = (Roman) livreTrouve;
            JOptionPane.showMessageDialog(this, roman.toString(), "Détails du Roman", JOptionPane.INFORMATION_MESSAGE);
        } else if (livreTrouve instanceof Essai) {
            Essai essai = (Essai) livreTrouve;
            JOptionPane.showMessageDialog(this, essai.toString(), "Détails de l'Essai", JOptionPane.INFORMATION_MESSAGE);
        } else if (livreTrouve instanceof LivreAudio) {
            LivreAudio livreAudio = (LivreAudio) livreTrouve;
            JOptionPane.showMessageDialog(this, livreAudio.toString(), "Détails du Livre Audio", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Type de livre non reconnu.", "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        // Afficher un message d'erreur si le livre n'a pas été trouvé
        JOptionPane.showMessageDialog(this, "Aucun livre trouvé avec cet ISBN.", "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1MouseClicked

